class RPG::Class

  #--------------------------------------------------------------------------
  # * exp_for_level
  #--------------------------------------------------------------------------
  def exp_for_level(level)
    lv = level.to_i
    basis = 5
    extra = 5
    acc_a = 5
    acc_b = 5
    return (basis * ((lv - 1) * (0.9 + acc_a / 250)) * lv * (lv + 1) / (6 + lv * 2 / 50 / acc_b) + (lv - 1) * extra)
  end

end #RPG::Class
